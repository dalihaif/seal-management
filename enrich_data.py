def infer_dept(name):
    """根据印章名称推断所属部门"""
    if not name or name.strip() == '' or name == '空号':
        return ''
    for keyword, dept in DEPT_RULES:
        if keyword in name:
            return dept
    # 如果包含"附属医院"或"临床医学院"前缀但没匹配到具体部门
    if '附属医院' in name or '临床医学院' in name:
        return '院办公室'
    return ''

def enrich_record(record):
    """单条记录富化"""
    name = record.get('name', '')

    # 推断类型
    record['type'] = infer_type(name)

    # 推断部门
    record['dept'] = infer_dept(name)

    # 保管人（默认空，需后续手动填写）
    record['keeper'] = record.get('keeper', '')

    # 存放地点（默认空，需后续手动填写）
    record['location'] = record.get('location', '')

    # 有效截止日期（从endDate映射）
    end_date = record.get('endDate', '')
    record['validUntil'] = end_date if end_date else ''

    # 刻制日期（默认空）
    record['carvingDate'] = record.get('carvingDate', '')

    return record


def main():
    # 读取原始数据
    with open(DATA_FILE, 'r', encoding='utf-8') as f:
        records = json.load(f)

    print(f'共读取 {len(records)} 条印章记录')

    # 统计信息
    stats = {
        'total': len(records),
        'by_type': {},
        'by_dept': {},
        'by_status': {},
        'empty_name': 0,
        'no_dept_inferred': 0,
    }

    # 逐条富化
    enriched = []
    for r in records:
        er = enrich_record(r)
        enriched.append(er)

        # 统计
        name = er.get('name', '')
        t = er.get('type', '未知')
        d = er.get('dept', '(未识别)')
        s = er.get('status', '未知')

        stats['by_type'][t] = stats['by_type'].get(t, 0) + 1
        stats['by_dept'][d] = stats['by_dept'].get(d, 0) + 1
        stats['by_status'][s] = stats['by_status'].get(s, 0) + 1

        if not name or name.strip() == '':
            stats['empty_name'] += 1
        if not er.get('dept'):
            stats['no_dept_inferred'] += 1

    # 写回文件
    with open(OUTPUT_FILE, 'w', encoding='utf-8') as f:
        json.dump(enriched, f, ensure_ascii=False, indent=2)

    print(f'\n✅ 富化完成！已写入 {OUTPUT_FILE}')
    print(f'\n📊 统计摘要:')
    print(f'   总记录数: {stats["total"]}')
    print(f'   空名称记录: {stats["empty_name"]}')
    print(f'   未识别部门: {stats["no_dept_inferred"]}')

    print(f'\n📋 按类型分布:')
    for t, c in sorted(stats['by_type'].items(), key=lambda x: -x[1]):
        print(f'   {t}: {c}')

    print(f'\n🏥 按部门分布 (Top 20):')
    sorted_depts = sorted(stats['by_dept'].items(), key=lambda x: -x[1])
    for d, c in sorted_depts[:20]:
        print(f'   {d}: {c}')
    if len(sorted_depts) > 20:
        print(f'   ... 共 {len(sorted_depts)} 个部门')

    print(f'\n📊 按状态分布:')
    for s, c in sorted(stats['by_status'].items(), key=lambda x: -x[1]):
        print(f'   {s}: {c}')


if __name__ == '__main__':
    main()
